                  <p class="c-txt">ご高齢の方などで、最新の安全装備がついた新車を希望しつつも「いつまで乗れるだろうか」「免許の返納も考えないと」と思案されている方でも、<span class="bold">中途解約で<span class="thema-color">解約金が発生しない</span></span>ため心配することなくカーライフを送ることができます。</p>
