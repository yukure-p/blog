                  <h3 class="c-tit03">ポイント1. 契約満了時のキャッシュバック率が96％・平均20万円</h3>
                  <p class="c-txt">契約満了時の査定で<span class="bold thema-color">キャッシュバック率が<span class="txt-line">96％</span></span>と、高確率でキャッシュバックされています。</p>
                  <p class="c-txt"><span class="bold thema-color">平均キャッシュバック額は<span class="txt-line">約20万円</span></span>となっており、契約時の残価設定が適正な価格で設定されていることが解る数値と言えます。</p>

                  <p class="c-txt">キャッシュバックがあるということは、<span class="bold">「そもそもの残価設定が低いのでは？」「キャッシュバックよりも月々のリース料金を安くして欲しい」</span>と考える方もいるかもしれません。</p>
