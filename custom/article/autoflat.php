                  <h3 class="c-tit03" id="index3">ポイント3. オートフラット専用の任意自動車保険がお得</h3>
                  <p class="c-txt">オートフラットでは任意自動車保険を月額に含めることができ、さらに<span class="bold">リース契約期間と同年数の長期の任意自動車保険契約</span>となります。</p>
                  <p class="c-txt">長期契約ため、<span class="bold thema-color">単年契約よりも割安でお得</span>な契約となります。</p>
                  <p class="c-txt">通常リース契約では任意保険の契約は自身で行うことがほとんどで、契約の手間が増えたり支払いが別々になってしまいます。</p>
                  
                  
                  <h4 class="c-tit04">事故で保険を使っても保険料が変わらない！</h4>
                  <p class="c-txt">オートフラット専用自動車保険の場合、事故などで保険を利用しても<span class="bold thema-color">契約時の保険料のまま</span>で、保険料が翌年などに変わることはありません。
                  </p>
                  <p class="c-txt">また、<span class="bold">事故による全損時や盗難時にリース契約を中途解約するために中途解約金が発生</span>しますが、<span class="bold thema-color">中途解約金を保険で補償</span>することができます。</p>
                </div>

