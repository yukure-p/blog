                <div class="c-box-inner box" id="index4">
                  <h3 class="c-tit03">ポイント3. いつでもやめれる！解約しても解約金0円！！</h3>
                  <p class="c-txt">解約金フリープランでは、<span class="bold">申込時に月額5ヶ月分相当を支払う＜</span>ことで<span class="bold thema-color">中途解約がいつでもできる</span>というプランになります。</p>
                  <p class="c-txt"><span class="bold">他のリースでは契約期間中の中途解約ができないことがほとんど</span>で、<span class="bold">中途解約には残りのリース料金の支払いなどが発生</span>します。</p>
                  <p class="c-txt">3年という契約期間内で車が不要になる可能性がある場合には、利用しやすいプランと言えます。</p>
                  <p class="c-txt">ただし、<span class="bold">中途解約せずリース満了まで利用した場合に、申込時の月額5ヶ月分相当が無駄になってしまう</span>とも言えます。</p>
                  <p class="c-txt"><span class="bold">利用期間が不明な場合に選択肢</span>となるサービスと言えます。</p>
                </div>



