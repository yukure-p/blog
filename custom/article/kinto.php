                  <div class="c-box-inner">
                    <h4 class="c-tit04">初期費用フリープラン</h4>
                    <p class="c-txt">長めの契約年数を選択でき、<span class="bold thema-color">契約期間中に新たな車に乗り換える</span>ことができます。</p>
                    <p class="c-txt">ライフスタイルに変化があった場合などに車を乗り換えることができ、<span class="bold">その時のライフスタイルに合わせた車種に乗ることができる</span>がの特徴です。</p>
                    <p class="c-txt">また、<span class="bold">新しい車が発売されて興味が出た場合や、趣味の変化によって車を乗り換えたい場合などに対応できカーライフに幅ができるプラン</span>になっています。</p>
                  </div>

                  <div class="c-box-inner">
                    <h4 class="c-tit04">解約金フリープラン</h4>
                    <p class="c-txt">3年と短めの契約期間となっており、<span class="bold thema-color">契約期間中いつでも中途解約が可能</span>なプランとなっています。</p>
                    <p class="c-txt">単身赴任などで、<span class="bold">短期間での利用を想定しているが利用期間を延長する可能性がある場合</span>などに利用しやすいプランとなっています。</p>
                  </div>
