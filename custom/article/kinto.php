                <div class="c-box-inner box" id="index3">
                  <h3 class="c-tit03">ポイント2. 契約期間中でも乗り換えができる(のりかえGO)！</h3>
                  <p class="c-txt">初期費用フリープランで利用できる「のりかえGO」は、<span class="bold thema-color">契約期間中に別の車に乗り換え</span>ができるサービスです。</p>
                  <p class="c-txt"><span class="bold">他のリースでは契約期間中の車の乗り換えができないことがほとんど</span>ですが、契約期間中に乗り換えができるKINTOの<span class="bold">「のりかえGO」は珍しいサービ</span>といえます。</p>
                  <p class="c-txt"><span class="bold">契約期間が長くなるほどライフスタイルの変化が起こる可能性は高くなります</span>が、<span class="bold thema-color">「のりかえGO」は変化に準備しておくとこができます。</span></p>
                  <p class="c-txt">
                    <span class="bold">「ライフスタイルの変化」「新たな趣味に合わせて」「新車への興味」</span>などから車を乗り換えるといったことができ、<span class="bold thema-color">その時に合わせた必要な車に乗れることで、充実したカーライフが送れます。</span></p>
                  <p class="c-txt"><span class="bold">車を購入した場合</span>には、現在の車のローン費用や次の車の頭金といったことで<span class="bold">気軽に車を乗り換えることは簡単ではない</span>ですが、<span class="bold thema-color">契約期間中に乗り換えが手軽にできるのはKINTOならではのサービス</span>と言えます。</p>
                  <p class="c-txt">ただし、<span class="bold">のりかえGOはいつでも乗り換えられるわけではなく、乗り換えが可能な時期が決まっていたり、乗り換えに手数料が発生します。</span></p>
                  <p class="c-txt">下記に<span class="bold">のりかえGOの手数料と乗り換え可能時の表があるのでご確認ください。</span></p>
                  <p class="linktxtBlock01"><a href="#norikaego" class="link02">のりかえGOの乗り換え可能時期・手数料の表はコチラ</a></p>

