              <div class="c-box intro">
                <p class="c-txt">KINTOとは、<span class="bold">トヨタグループ</span>が展開するモビリティサービスのブランドになります。</p>
                <p class="c-txt">トヨタグループが展開しているブランドということもあり、<span class="bold">選べる車種はトヨタ車・レクサス・モリゾウセレクションのみ</span>となっています。</p>
                <p class="c-txt">KINTOは2つの契約プランが用意されており、その時の状況に合わせて車を利用しやすいプランから選択する形になっています。</p>
                <p class="c-txt"><span class="bold">「初期費用フリープラン」</span>　<span class="bold">「解約金フリープラン」</span></p>

                <p class="c-txt">プランによって<span class="bold">「契約中の車の乗り換え」「中途解約金の発生の有無」「契約満了後の車の選択肢」</span>などが変わってきます。</p>
                <p class="c-txt">ライフスタイルの変化に準備ができる「のりかえGO」や、利用期間が不明瞭な場合には中途解約ができるといった未来の状況に選択肢を持ったサービスとなっており、車を購入した場合や他のカーリースと比べても状況の変化に対応しやすいカーリースになっています。</p>
                <p class="c-txt">それでは、ポイントから説明していきます。</p>
              </div>



