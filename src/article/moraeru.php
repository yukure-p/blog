                <h2 class="c-tit02" >車がもらえるカーリースのメリット</h2>

                <div class="c-box-inner">
                  <p class="c-txt"><span class="bold txt-line">リースで車がもらえることは、もらえる以上のメリットがあります。</span></p>
                  <p class="c-txt">通常のカーリースでは、<span class="bold">リース期間中は常にリースしている車</span>という気持ちで利用することになり、<span class="bold">何かあったときにはリース満了後の査定のことを考えてしまう</span>ことになります。</p>
                  <p class="c-txt"><span class="bold">購入した車でも傷・ヘコミなど付けたときは普通にショック</span>を受けると思いますが、カーリースであれば<span class="bold txt-line">リース満了後の査定のことを想うと二重のショック</span>となります。</p>
                  <p class="c-txt">しかし、最終的に車がもらえることで<span class="bold">リース満了後のことを気にしなくて良くなり、<span class="txt-line">「走行距離」「車の傷」「ヘコミ」「車のカスタムや改造」「ペットの同乗」「喫煙」などを気にする必要がなくなり</span>購入した車と同じように利用でき、且つカーリースのメリットである月々定額の支払いなので、単純にもらえるという以上のメリット</span>といえます。</p>
