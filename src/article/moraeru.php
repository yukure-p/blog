                <h2 class="c-tit02">車がもらえるカーリースの注意点</h2>
                <div class="c-box-inner">
                  <p class="c-txt">車がもらえるカーリースではもらえることによって、上記で記載したメリットが多くあります。</p>

                  <p class="c-txt">しかし、車がもらえるカーリースでも注意点があります。<br>車がもらえるリースの場合、リース契約期間が長期の契約となる場合が多くなっており、もらえるカーリースMOTAでは5年の契約で車がもらえますが、そのほかのリースは7年や9年といった長期の契約となっています。</p>
                  <p class="c-txt">契約期間が長くなればなるほど、中途解約のリスクが高くなっていくと言えます。</p>
                  <p class="c-txt">ライフスタイルの変化や、趣味の変更などといったことで車を変更することができないのが長期の契約なので、注意する必要があります。</p>
                  <p class="c-txt">また、もらえることにより車のカスタムや、ペットの同乗、喫煙、走行距離といったことも気にせず利用することができますが、もし中途解約するとなった場合には原状回復費用が通常の利用時よりも掛かってくるので、中途解約に至らないように契約年数をよく考慮する必要があります。</p>
                </div>

