                <div class="c-box-inner">
                  <h3 class="c-tit03">オープンエンド契約</h3>
                  
                  <p class="c-txt">オープンエンド契約とは<span class="bold txt-line">残価を明示する契約に</span>なり、リース満了後には車の「買取」「再リース」「返却」といった選択肢があります。</p>
                  <p class="c-txt">残価が高く設定されている場合は、月々のリース料金の支払が割安になり、残価が低い場合は月々のリース料金は割高になります。</p>

                  <p class="c-txt">人気車種の場合などはリース満了後も車両の価値が下がりにくいので、残価が高く設定されやすい傾向にあります。</p>
                  
                  <p class="c-txt">オープンエンド契約では<span class="bold txt-line">リース満了時に車の査定を行い、リース満了時点の車の価値を改めて算出</span>します。</p>
                  <p class="c-txt">リース満了後の査定額が残価よりマイナスの査定であった場合は追加の支払いが発生し、プラスの査定であった場合は追加の支払いが発生なし、もしくはプラス分がキャッシュバックされるというリース会社もあります。</p>

                  <p class="c-txt">例えばリース満了後に車の買取をしたい場合に、マイナス査定の場合は「追加の支払い＋残価」を支払うことで買取ができます。</p>


                  <p class="c-txt">オープンエンド契約ではリース満了時点の車の査定額を改めて出すので、<span class="bold txt-line">契約期間中に市場動向に変化</span>があった場合などに査定額に影響が出てくるといった特徴があります。</p>
                  <p class="c-txt">残価が高く設定されている方が、月々のリース料金が割安になってお得に感じるかもしれませんが、リース満了後の査定のことも考える必要があるのがオープンエンド契約となります。</p> 
