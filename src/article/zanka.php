              <div class="c-box">
                <div class="c-box-inner box" id="index1">
                  <h2 class="c-tit02">残価とは？</h2>
                  <p class="c-txt">残価とは「残存価格」の略となり、<span class="bold txt-line">リース契約満了後のクルマの価値</span>のことを言います。<br>
                    車の車種・カラー・グレード・想定走行距離などからリース満了後に車がどのくらいの価値があるかを予測し、設定する価格が残価となります。</p>
                  <p class="c-txt">車体価格から、残価を差し引いた金額が月々のリース料金となります。</p>
                  <p class="c-txt"></p>
                  <p class="c-txt">この<span class="bold txt-line">残価を明示するリース契約を「オープンエンド契約」</span>と言い、<span class="bold txt-line">残価を明示しない契約を「クローズドエンド契約」</span>と言います。</p>
                </div>

